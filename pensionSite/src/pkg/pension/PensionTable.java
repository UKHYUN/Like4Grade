package pkg.pension;

public class PensionTable {  // 임시코드. DB 구현후 다시 수정하기.
	private String pension_name ;
	private String local_code ;
	private String pay_code ;
	private String thema_code ;
	
	public String getPension_name() {
		return pension_name;
	}
	public void setPension_name(String pension_name) {
		this.pension_name = pension_name;
	}
	public String getLocal_code() {
		return local_code;
	}
	public void setLocal_code(String local_code) {
		this.local_code = local_code;
	}
	public String getPay_code() {
		return pay_code;
	}
	public void setPay_code(String pay_code) {
		this.pay_code = pay_code;
	}
	public String getThema_code() {
		return thema_code;
	}
	public void setThema_code(String thema_code) {
		this.thema_code = thema_code;
	}
	
	
}
